describe('Yum page (delimar.bandcamp.com/yum)', () => {
  let blob: unknown
  beforeAll(async () => { blob = await fetchBlob('https://delimar.bandcamp.com/yum') })

  it('is recognized as YumPageBlob', () => {
    expect(isYumPage(blob), 'isYumPage guard failed').toBe(true)
  })

  it('mband key drift', () => {
    if (!isYumPage(blob)) return
    const missing = drift(blob.mband as object, MBAND_KEYS)
    expect(missing, `Typed in MBand but absent from blob: ${missing.join(', ')}`).toHaveLength(0)
  })

  it('mband type sanity', () => {
    if (!isYumPage(blob)) return
    expect(typeof blob.mband.id).toBe('number')
    expect(typeof blob.mband.subdomain).toBe('string')
    expect(typeof blob.mband.is_label).toBe('boolean')
    expect(Array.isArray(blob.mband.navbar_items)).toBe(true)
  })

  it('identities key drift', () => {
    if (!isYumPage(blob)) return
    const missing = drift(blob.identities as object, YUM_IDENTITIES_KEYS)
    expect(missing, `Typed in YumIdentities but absent from blob: ${missing.join(', ')}`).toHaveLength(0)
  })

  it('fan_onboarding key drift (when present)', () => {
    if (!isYumPage(blob) || !blob.fan_onboarding) return
    const missing = drift(blob.fan_onboarding as object, FAN_ONBOARDING_KEYS)
    expect(missing, `Typed in FanOnboarding but absent from blob: ${missing.join(', ')}`).toHaveLength(0)
  })
})

